

public class Puzzle {
	
	
	
	public String category;
	public String phrase;

    public Puzzle(String c, String p) {
    	
    	category = c;
    	phrase = p;
    	
    }
    
    
    public String getPhrase(){
    	
    	return phrase;
    	
    }
    
    
    public String getCategory(){
    	
    	return category;
    	
    }
    
}